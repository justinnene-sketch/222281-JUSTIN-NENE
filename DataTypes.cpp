//This program demonstrates the use of various data types in C++17
#include <iostream>
using namespace std;

int main() 
{
    // Integer data type
    int intVar = 42; // Integer variables store whole numbers without decimal points

    // Floating-point data type
    float floatVar = 3.14; // Float variables store numbers with decimal points

    // Double data type
    double doubleVar = 2.718281828459;
    cout << "Float: " << floatVar << endl;
    cout << "Double: " << doubleVar << endl;

    // Character data type
    char charVar = 'A';
    cout << "Character: " << charVar << endl;

    // Boolean data type
    bool boolVar = true;
    cout << "Boolean: " << boolVar << endl;

    // String data type
    string strVar = "Hello, C++!";
    cout << "String: " << strVar << endl;

    return 0;
}